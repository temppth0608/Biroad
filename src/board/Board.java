package board;

public class Board {

	private int boardId;
	private String boardTitle;
	private String boardText;
	private String boardMemb;
	private String boardPass;
	private String boardDate;
	private int heart;
	private String boardRoad;
	private String image;
	
	public int getBoardId() {
		return boardId;
	}
	public void setBoardId(int boardId) {
		this.boardId = boardId;
	}
	public String getBoardTitle() {
		return boardTitle;
	}
	public void setBoardTitle(String boardTitle) {
		this.boardTitle = boardTitle;
	}
	public String getBoardText() {
		return boardText;
	}
	public void setBoardText(String boardText) {
		this.boardText = boardText;
	}
	public String getBoardMemb() {
		return boardMemb;
	}
	public void setBoardMemb(String boardMemb) {
		this.boardMemb = boardMemb;
	}
	public String getBoardPass() {
		return boardPass;
	}
	public void setBoardPass(String boardPass) {
		this.boardPass = boardPass;
	}
	public String getBoardDate() {
		return boardDate;
	}
	public void setBoardDate(String boardDate) {
		this.boardDate = boardDate;
	}
	public int getHeart() {
		return heart;
	}
	public void setHeart(int heart) {
		this.heart = heart;
	}
	public String getBoardRoad() {
		return boardRoad;
	}
	public void setBoardRoad(String boardRoad) {
		this.boardRoad = boardRoad;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
}
